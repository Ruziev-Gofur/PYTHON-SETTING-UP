# assigning 2 variables a, b
a, b = int(input('a: ')), int(input('b: '))

print(a + b)  # addition
print(a - b)  # subtraction
print(a * b)  # multiplication
print(a / b)  # division
print(a // b)  # floor division
print(a % b)  # modulus
print(a ** b)  # exponentiation
print(pow(a, b))  # exponentiation using pow function

